DEFAULT_ANNOTATION_TEXT = """Аннотация блоков:
HLA-гаплотипы (класс I)
Иммуногенетический профиль пациента может влиять на эффективность иммунного распознавания опухоли и, как следствие, на исходы при иммунотерапии ингибиторами контрольных точек (ИКИ).
Некоторые аллели HLA-I ассоциированы с более благоприятным прогнозом и повышенной вероятностью объективного ответа на ИКИ при меланоме: HLA-B*44, HLA-B*35 и HLA-A*02 потенциально связаны с эффективной презентацией опухолевых неоантигенов и усиленным Т-клеточным ответом (PMID: 29217585).
Напротив, аллели HLA-B*15 и HLA-B*62 наблюдались чаще у пациентов с более короткой общей выживаемостью, что может отражать ограничения в репертуаре презентируемых пептидов (PMID: 29217585).
Особое внимание заслуживает гомозиготность по локусам HLA-I (HLA-A, -B или -C): у таких пациентов отмечают сокращение общей выживаемости, вероятно, вследствие снижения разнообразия презентируемых антигенов и ослабления противоопухолевого иммунного ответа (PMID: 39981665).

Опухолевая мутационная нагрузка (TMB)
Прогностический биомаркер при меланоме. Высокий уровень TMB > 10 (TMB-H) может коррелировать с более выраженным ответом на ингибиторы иммунных контрольных точек, как при монотерапии анти-PD-1, так и при комбинированных схемах, и более благоприятными показателями выживаемости у пациентов с меланомой.
Пембролизумаб одобрен как препарат, не зависящий от тканевого происхождения, для лечения твердых опухолей с высоким уровнем TMB. TMB отражает общее количество кодирующих соматических мутаций в опухоли, которые потенциально могут распознаваться иммунной системой организма как чужеродные и подвергаться атаке. (PMID: 26450194, 31406485, 35355708)

Альтерации гена BRAF
Позволяют выявить пациентов, подходящих для таргетной терапии, направленной против BRAF и/или MEK, при кожной меланоме. Активирующие мутации BRAF V600 делают опухоль чувствительной к ингибиторам BRAF и MEK, и именно у пациентов с такими мутациями чаще наблюдается наибольшая польза от комбинированной терапии.
Мутации BRAF, не затрагивающие кодон 600 (например, L597, K601), также могут быть чувствительны к ингибированию пути BRAF/MEK, хотя их ответ на терапию требует дополнительного подтверждения.
Ген BRAF кодирует протеинкиназу, участвующую в сигнальном пути, регулирующем рост и выживаемость клеток. (PMID: 28572459, 21343559)

Альтерации гена NRAS
Связаны с более агрессивным течением и худшими показателями выживаемости при кожной меланоме. У пациентов с мутациями NRAS, у которых развилась метастатическая или неоперабельная болезнь после терапии ингибиторами иммунных контрольных точек, может рассматриваться применение ингибиторов MEK, таких как биниметиниб.
Однако монотерапия ингибиторами MEK до сих пор не продемонстрировала выраженной эффективности, поэтому в настоящее время активно изучаются различные комбинированные схемы лечения.
Ген NRAS кодирует фермент, активирующий сигнальные пути, способствующие росту и пролиферации клеток. (PMID: 28572459, 22180178, 29785260)

Альтерации гена NF1
Являются перспективными биомаркерами при меланоме. Мутации потери функции в NF1 могут ассоциироваться с резистентностью к ингибиторам BRAF и MEK, а также с повышенным риском летального исхода и худшими показателями выживаемости.
Опухоли с мутациями NF1 иногда характеризуются более высокой мутационной нагрузкой (TMB), что теоретически может создавать предпосылки для более выраженного ответа на иммунотерапию — аналогично подходу, применяемому при лечении пациентов с диким типом NF1.
Ген NF1 кодирует опухолевый супрессор, который подавляет сигнальные пути, зависимые от Ras, и тем самым ограничивает рост и пролиферацию клеток. (PMID: 28572459)

Молекулярный подтип triple-WT (BRAF/NF1/RAS дикого типа)
Характеризуется отсутствием драйверных мутаций в ключевых генах сигнальных путей BRAF, NRAS/KRAS/HRAS и NF1. Этот подтип составляет меньшинство кожных меланом (~10–15 %), но может быть более распространён в мукозальных и увеальных формах.
Triple-WT опухоли часто демонстрируют гетерогенный молекулярный профиль: могут встречаться альтернативные драйверные альтерации — например, мутации в KIT, GNAQ/GNA11, MAP2K1, ARID2, а также генные фьюжны (ALK, ROS1, NTRK, RET). У части пациентов выявляются структурированные хромосомные перестройки или признаки активации PI3K/AKT-пути.
• Ответ на ингибиторы BRAF/MEK маловероятен в отсутствие соответствующих мутаций.
• Некоторые triple-WT опухоли демонстрируют средний или высокий TMB и признаки T-cell-inflamed микросреды, что может указывать на потенциальную чувствительность к иммунотерапии.
• Однако клинические исходы у этой группы остаются неоднородными, и прогностическая значимость подтипа требует стратификации по дополнительным биомаркерам (например, HLA-статус, TMB, PD-L1).
• При выявлении triple-WT статуса рекомендуется расширенное NGS-панельное тестирование для поиска редких, но потенциально целевых альтераций. (PMID: 34680367)"""


def _read_annotation_text(path: Path) -> str:
    if not path.exists():
        return DEFAULT_ANNOTATION_TEXT
    try:
        text = path.read_text(encoding="utf-8")
        return text if text.strip() else DEFAULT_ANNOTATION_TEXT
    except Exception:
        return DEFAULT_ANNOTATION_TEXT


def render_annotation_blocks() -> None:
    sample = st.session_state.get("selected_sample", "")
    if not sample:
        return

    text = _read_annotation_text(annotation_blocks_path(sample))
    lines = [line.rstrip() for line in text.splitlines()]

    html_parts = ['<div class="annotation-blocks-wrap">']
    for line in lines:
        stripped = line.strip()
        if not stripped:
            html_parts.append('<div class="annotation-blocks-gap"></div>')
            continue
        if stripped.endswith(":"):
            html_parts.append(f'<div class="annotation-blocks-title">{esc(stripped)}</div>')
        elif stripped in {
            "HLA-гаплотипы (класс I)",
            "Опухолевая мутационная нагрузка (TMB)",
            "Альтерации гена BRAF",
            "Альтерации гена NRAS",
            "Альтерации гена NF1",
            "Молекулярный подтип triple-WT (BRAF/NF1/RAS дикого типа)",
        }:
            html_parts.append(f'<div class="annotation-blocks-subtitle">{esc(stripped)}</div>')
        elif stripped.startswith("•"):
            html_parts.append(f'<div class="annotation-blocks-bullet">{esc(stripped)}</div>')
        else:
            html_parts.append(f'<div class="annotation-blocks-text">{esc(stripped)}</div>')
    html_parts.append('</div>')
    st.markdown("".join(html_parts), unsafe_allow_html=True)
